/**
 * AI Agent Page - Main page for managing integrations
 * Requirements: 8.1, 8.3
 */

import { useState } from 'react';
import { motion } from 'framer-motion';
import ChannelsTab from './ChannelsTab';
import CRMTab from './CRMTab';
import PromptsTab from './PromptsTab';
import BotSettingsTab from './BotSettingsTab';
import LocationsTab from './LocationsTab';
import PromotionsTab from './PromotionsTab';
import RestrictionsTab from './RestrictionsTab';

type TabId = 'channels' | 'crm' | 'prompts' | 'settings' | 'locations' | 'promotions' | 'restrictions';

interface Tab {
  id: TabId;
  name: string;
  icon: React.FC<{ className?: string }>;
}

const tabs: Tab[] = [
  { id: 'channels', name: 'Каналы связи', icon: ChannelsIcon },
  { id: 'crm', name: 'CRM', icon: CRMIcon },
  { id: 'prompts', name: 'Промты', icon: PromptsIcon },
  { id: 'settings', name: 'Настройки бота', icon: SettingsIcon },
  { id: 'locations', name: 'Локации', icon: LocationsIcon },
  { id: 'promotions', name: 'Промо', icon: PromotionsIcon },
  { id: 'restrictions', name: 'Ограничения', icon: RestrictionsIcon },
];

export default function AgentPage() {
  const [activeTab, setActiveTab] = useState<TabId>('channels');

  return (
    <div className="space-y-6">
      {/* Header */}
      <motion.div
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
      >
        <h1 className="text-2xl font-bold text-gray-900 dark:text-white">
          ИИ Агент
        </h1>
        <p className="text-gray-600 dark:text-gray-400 mt-1">
          Управление каналами связи, CRM-интеграциями и промтами бота
        </p>
      </motion.div>

      {/* Tabs */}
      <motion.div
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ delay: 0.1 }}
        className="border-b border-gray-200 dark:border-gray-700"
      >
        <nav className="-mb-px flex space-x-8">
          {tabs.map((tab) => (
            <button
              key={tab.id}
              onClick={() => setActiveTab(tab.id)}
              className={`
                flex items-center gap-2 py-4 px-1 border-b-2 font-medium text-sm transition-colors
                ${activeTab === tab.id
                  ? 'border-orange-500 text-orange-600 dark:text-orange-400'
                  : 'border-transparent text-gray-500 hover:text-gray-700 hover:border-gray-300 dark:text-gray-400 dark:hover:text-gray-300'
                }
              `}
            >
              <tab.icon className="w-5 h-5" />
              {tab.name}
            </button>
          ))}
        </nav>
      </motion.div>

      {/* Tab Content */}
      <motion.div
        key={activeTab}
        initial={{ opacity: 0, y: 10 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ duration: 0.2 }}
      >
        {activeTab === 'channels' && <ChannelsTab />}
        {activeTab === 'crm' && <CRMTab />}
        {activeTab === 'prompts' && <PromptsTab />}
        {activeTab === 'settings' && <BotSettingsTab />}
        {activeTab === 'locations' && <LocationsTab />}
        {activeTab === 'promotions' && <PromotionsTab />}
        {activeTab === 'restrictions' && <RestrictionsTab />}
      </motion.div>
    </div>
  );
}

